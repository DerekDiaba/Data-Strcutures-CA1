    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        
        //Get input from text fields
        String name = txtName.getText();
        String ratingText = txtRating.getText();
        String notes = txtNotes.getText();

//  check if name or rating fields are empty
        if (name.isEmpty() || ratingText.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please fill in Name and Rating");
        return;
       }

       int rating;

       try {
          //Convert rating ftom text to Integer
         rating = Integer.parseInt(ratingText);
     }  catch (NumberFormatException e) {
         //Show error is user enter a rating that is non numeric
    javax.swing.JOptionPane.showMessageDialog(this, "Rating must be a number");
    return;
    }
       //Get Priority from dropdown
    String priority = PriorityCB.getSelectedItem().toString();
    
    // create a new AccessibleLocation object
    AccessibleLocation loc = new AccessibleLocation(name, rating, notes,priority);
    
    // Add location to the manger that handles Linked List Queue Stack
    manager.addLocation(loc);

      refreshDisplay();   //refresh the display area to show updated list

 
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
        //Prompt user to enter the index of Location to delete
      String input = javax.swing.JOptionPane.showInputDialog("Enter index to delete:");
      
      //convert the input string to an integer
      int index = Integer.parseInt(input);

    manager.deleteLocation(index); // Call the manager to delete location at the given index also pushes the deleted location onto the undoStack

      refreshDisplay();  // refresh the display area to show the updated lists of locations
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        
        // Propmpt the user to enter the index of the location to update
       String input = JOptionPane.showInputDialog("Enter index to update:");
       
        // If user cancels or leaves inputy empty do nothing
        if(input == null || input.isEmpty()) return;
        int index = Integer.parseInt(input);// convert the input string to an integer index
        
        
        // Retrieve values from the input fields
        String name = txtName.getText();
        String ratingText = txtRating.getText();
        String notes = txtNotes.getText();

        //Make sure that name and rating is not empty
        if(name.isEmpty() || ratingText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Fill Name and Rating");
            return;
        }

        int rating = Integer.parseInt(ratingText); // Convert rating from text to integer
            String priority = PriorityCB.getSelectedItem().toString(); // Get selected priority from dropdown
        AccessibleLocation loc = new AccessibleLocation(name, rating, notes, priority); // Create a new AccessibleLocation object with updated details
        manager.updateLocation(index, loc); // Update the location at the specified index in the manager
       refreshDisplay(); // refresh the display area to reflect the updated list
    }//GEN-LAST:event_btnUpdateActionPerformed

