    private void refreshDisplay() {
        //clear text area before updating
    txtDisplay.setText("");

    int i = 0;
    
    //Loop through all locations in the manager
    for (Location loc : manager.getAllLocations()) {
        
        //Append each locations details to the display area
        // Show the index and the details 
        txtDisplay.append(i + ": " + loc.displayDetails() + "\n");
        i++; //increment for the next location
    }
}




